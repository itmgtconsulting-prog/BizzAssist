/**
 * GET  /api/links?type=company&id=12345678 — hent links for en entitet
 * POST /api/links — opret eller verificer et link
 *
 * Håndterer crowdsourced link-verificering for virksomheder og personer.
 * Links gemmes i Supabase (verified_links + link_verifications).
 * Fallback til auto-genererede links hvis Supabase ikke er tilgængelig.
 *
 * @param type - 'company' eller 'person'
 * @param id - CVR-nummer (company) eller enhedsNummer (person)
 */

import { NextRequest, NextResponse } from 'next/server';
import { z } from 'zod';
import { createClient } from '@supabase/supabase-js';
import { parseBody } from '@/app/lib/validate';
import { writeAuditLog } from '@/app/lib/auditLog';

/** Zod schema for POST /api/links request body */
const linksPostSchema = z
  .object({
    action: z.string().optional(),
    userId: z.string().min(1),
  })
  .passthrough();

const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL ?? '';
const SUPABASE_SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY ?? '';

